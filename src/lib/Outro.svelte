    <p>Whether it be ink <span style = 'color: #7695c4; font-weight: 600'>pens</span>, packs of <span style = 'color: #7695c4; font-weight: 600'>rice</span>, tubes of <span style = 'color: #7695c4; font-weight: 600'>toothpaste</span>, or <span style = 'color: #7695c4; font-weight: 600'>15-minute telephone calls</span>, prison prices and the amount of labor required to be able to afford these prices clearly differ across US states. Not because ink pens, packs of rice, tubes of toothpaste, or 15-minute telephone calls are more or less expensive in absolute across US states, but because a lack of external oversight encourages prisons and their conditions to vary.<br><br>And, in recent years, while some US states have created external independent entities that monitor and report on statewide conditions inside correctional facilities, other states have not. As a result, some US states subject correctional facilities to improved transparency and increased accountability, while others don’t. So, different prison conditions, related to wages, working conditions, and even commissary prices, can exist in different US states, thus contributing to even further inequality within the US prison system.
