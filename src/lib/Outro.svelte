    <p>But why the discrepancy? Whether it be ink <span style = 'color: #7695c4; font-weight: 700'>pens</span>, packs of <span style = 'color: #7695c4; font-weight: 700'>rice</span>, tubes of <span style = 'color: #7695c4; font-weight: 700'>toothpaste</span>, or <span style = 'color: #7695c4; font-weight: 700'>15-minute telephone calls</span>, prison prices and the amount of labor required to be able to afford these prices clearly differ across states. Not because ink <span style = 'color: #7695c4; font-weight: 700'>pens</span>, packs of <span style = 'color: #7695c4; font-weight: 700'>rice</span>, tubes of <span style = 'color: #7695c4; font-weight: 700'>toothpaste</span>, or <span style = 'color: #7695c4; font-weight: 700'>15-minute telephone calls</span> are more or less expensive in absolute across states, but because a lack of external oversight encourages prisons and their conditions to vary.<br><br>And, in recent years, while some states have created external independent entities that monitor and report on statewide conditions inside correctional facilities, other states have not. This basically means that some states subject correctional facilities to improved transparency and increased accountability, while others don’t. So, different prison conditions, related to wages, working conditions, and even commissary prices, can exist in different states, thus contributing to even further inequality within the US prison system.
    </p>
    <p>Thus, as demonstrated by this piece, all incarcerated people in all states are not only subjected to unfair labor practices when compared to non-incarcerated people in their same state, but they are also subjected to inequitable compensation when compared to other incarcerated people employed in other states. <br><br>So, what’s a dollar cost for folks subjected to US incarceration?<br><br>That greatly and unjustly depends on the state in which someone is experiencing incarceration.</p>
